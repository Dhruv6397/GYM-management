    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id =jLabel3.getText();
        String name=jTextField1.getText();
        String mobilenumber=jTextField2.getText();
        String email=jTextField3.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        String time=(String)jComboBox2.getSelectedItem();
        String aadharnumber=jTextField4.getText();
        String age=jTextField5.getText();
        String amount=jTextField6.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into Gymmember values(?,?,?,?,?,?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, mobilenumber);
            ps.setString(4, email);
            ps.setString(5, gender);
            ps.setString(6, time);
            ps.setString(7, aadharnumber);
            ps.setString(8, age);
            ps.setString(9, amount);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Saved Successfully!");
            setVisible(false);
            new NewMember().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

